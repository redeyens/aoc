        }

        private static Monster ParseMonster()
        {
            var nodes = GetLines(monsterTemplate)
                .SelectMany((l, i) => l.Select((c, j) => (i, j, c)))
                .Where(n => n.c == '#');
            
            return new Monster(nodes);
        }

        private static TileFamily ParseTile(string tileDef)
        {
            var parts = tileDef.Split(':');

            int id = Int32.Parse(parts[0].Trim());

            var firstLine = string.Empty;
            var firstColumn = new char[10];
            var lastColumn = new char[10];
            var lastLine = string.Empty;
            int lineCount = 0;
            var contentBldr = new StringBuilder();

            foreach (var line in GetLines(parts[1]).Where(l => !string.IsNullOrWhiteSpace(l)))
            {
                if(string.IsNullOrEmpty(firstLine))
                {
                    firstLine = line;
                }
                lastLine = line;
                firstColumn[lineCount] = line[0];
                lastColumn[lineCount] = line[line.Length - 1];

                if(lineCount > 0 && lineCount < (line.Length - 1))
                {
                    contentBldr.AppendLine(line.Trim().Substring(1, line.Length - 2));  
                }

                lineCount++;
            }

            var firstC = new string(firstColumn);
            var lastC = new string(lastColumn);

            return new TileFamily(id, lastC, firstLine, firstC, lastLine, contentBldr.ToString());
        }

        private static IEnumerable<string> GetLines(string input)
	    {
            var inputReader = new StringReader(input);
            string currentLine = null;
                    
            while((currentLine = inputReader.ReadLine()) != null)
                yield return currentLine;
	    }
	
